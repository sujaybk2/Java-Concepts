package Sprint4;
//The do...while loop example is below, where the code is run first, then condition is checked.
public class LoopPracticeDoWhile1 {
    public static void main(String[] args){
        int i=11;

        do {

                    System.out.println("Hello, World!");

        } while(i<10);

        System.out.println("Loop is finished.");

    }
}
