package Sprint1;

public class BasicCalculationExamples {
    public static void main(String [] args){
        double number1=50.79;
        int number2=15;
        double sum= number1 + number2, difference= number1-number2, product= number1*number2, quotient= number1/number2;
        System.out.println("The sum of both values is: ");
        System.out.println(sum);
        System.out.println("The difference of both values is: ");
        System.out.println(difference);
        System.out.println("The product of both values is: ");
        System.out.println(product);
        System.out.println("The quotient of both values is: ");
        System.out.println(quotient);
    }
}
